def textToBanner(text) :
	# text = unicodedata.normalize('NFKD', text).encode('ascii', 'ignore').decode('utf-8')
	text = ''.join(c for c in unicodedata.normalize('NFD', text) if unicodedata.category(c) != 'Mn')
	text = text.lower()
	extra_line = 'q' in text
	res = [[] for i in range(6 if extra_line else 5)]
	for c in text : 
		if c in font :
			letter = font[c]
			for i in range(5) :
				res[i].append(letter[i])
			if extra_line : 
				res[5].append(letter[5] if c == 'q' else " " * len(letter[0]))
	return [' '.join(l) for l in res]

